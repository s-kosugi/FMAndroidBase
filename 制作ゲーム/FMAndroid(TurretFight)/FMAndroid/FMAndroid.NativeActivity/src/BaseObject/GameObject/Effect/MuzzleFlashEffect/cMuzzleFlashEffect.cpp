#include "cMuzzleFlashEffect.h"

//==========================================================================================
// 定数
//==========================================================================================
const int	cMuzzleFlashEffect::PRIORITY = 400;

//==========================================================================================
// コンストラクタ
//==========================================================================================
cMuzzleFlashEffect::cMuzzleFlashEffect(IBaseObject * parent)
	: IEffect(parent, "BomEffect", "data\\graphic\\muzzle.png")
{
	SetPriority(PRIORITY);
}

//==========================================================================================
// 初期化
//==========================================================================================
void cMuzzleFlashEffect::Initialize(const cVector2 & pos)
{
	// アニメーション初期設定
	SetupAnime({ 256,256 }, 4, 15, 0.03f);

	IEffect::Initialize(pos);
}
