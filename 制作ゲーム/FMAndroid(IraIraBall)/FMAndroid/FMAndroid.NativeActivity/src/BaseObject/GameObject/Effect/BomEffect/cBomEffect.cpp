#include "cBomEffect.h"

//==========================================================================================
// 定数
//==========================================================================================
const int	cBomEffect::PRIORITY = 400;

//==========================================================================================
// コンストラクタ
//==========================================================================================
cBomEffect::cBomEffect(IBaseObject * parent)
	: IEffect(parent, "BomEffect", "data\\graphic\\bom_effect.png")
{
	SetPriority(PRIORITY);
}

//==========================================================================================
// 初期化
//==========================================================================================
void cBomEffect::Initialize(const cVector2 & pos)
{
	// アニメーション初期設定
	SetupAnime({128,128},4,11,0.05f);

	IEffect::Initialize(pos);
}
