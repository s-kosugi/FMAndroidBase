#include "cStageStart.h"
#include "..\..\Player\cPlayer.h"

//==========================================================================================
// 定数
//==========================================================================================
const int	cStageStart::PRIORITY = 300;

//==========================================================================================
// コンストラクタ
//==========================================================================================
cStageStart::cStageStart(IBaseObject * parent)
	: IStage(parent, "StageStart", "data\\graphic\\home.png")
{
}

//==========================================================================================
// デストラクタ
//==========================================================================================
cStageStart::~cStageStart(void)
{
}

//==========================================================================================
// 初期化
//==========================================================================================
void cStageStart::Initialize(const cVector2 & pos)
{
	SetPriority(PRIORITY);
	// 当たり判定を設定
	m_fDist = GetSpriteSize().x / 2.0f;

	cPlayer* player = (cPlayer*)GetParent()->FindSibling("Player");
	if (player)
	{
		// プレイヤーの位置を変更する
		player->SetPos(pos);
	}

	IStage::Initialize(pos);
}

//==========================================================================================
// 更新
//==========================================================================================
void cStageStart::Update(void)
{
	IStage::Update();
}

