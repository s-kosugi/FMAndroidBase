#include "cEnmWhite.h"
#include "cGame.h"

//==========================================================================================
// 定数
//==========================================================================================
const int	cEnmWhite::PRIORITY = 300;
const float cEnmWhite::BASE_SPEED = 2.5f;
const float cEnmWhite::ROTATE_SPEED = 8.0f;

//==========================================================================================
// コンストラクタ
//==========================================================================================
cEnmWhite::cEnmWhite(IBaseObject * parent)
	: IEnemy(parent, "EnmWhite", "data\\graphic\\enemy_01.png")
{
}

//==========================================================================================
// デストラクタ
//==========================================================================================
cEnmWhite::~cEnmWhite(void)
{
}

//==========================================================================================
// 初期化
//==========================================================================================
void cEnmWhite::Initialize(const cVector2 & pos)
{

	SetPriority(PRIORITY);
	// 当たり判定を設定
	m_fDist = GetSpriteSize().x / 2.0f;

	// 位置を設定する
	m_vPos = pos;

	// 画面外配置処理
	ArrangeOffScreen();

	// 現在位置からプレイヤーに向かって移動させる
	GotoPlayer(BASE_SPEED);

	IEnemy::Initialize(m_vPos);
}

//==========================================================================================
// 更新
//==========================================================================================
void cEnmWhite::Update(void)
{
	// 自転処理
	SetAngle(GetAngle() + ROTATE_SPEED);

	IEnemy::Update();
}
