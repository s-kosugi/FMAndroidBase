#include "cLaserEffect.h"

//==========================================================================================
// 定数
//==========================================================================================
const int	cLaserEffect::PRIORITY = 400;

//==========================================================================================
// コンストラクタ
//==========================================================================================
cLaserEffect::cLaserEffect(IBaseObject * parent)
	: IEffect(parent, "LaserEffect", "data\\graphic\\laser.png")
{
	SetPriority(PRIORITY);
}

//==========================================================================================
// 初期化
//==========================================================================================
void cLaserEffect::Initialize(const cVector2 & pos)
{

	// アニメーション初期設定
	SetupAnime({ 256,256 }, 4, 12, 0.03f);

	IEffect::Initialize(pos);
}
