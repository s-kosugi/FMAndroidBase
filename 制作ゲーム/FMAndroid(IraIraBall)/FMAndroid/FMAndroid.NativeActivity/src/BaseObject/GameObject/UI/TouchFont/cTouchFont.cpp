#include "cTouchFont.h"
#include "cGame.h"
#include "Utility/Timer/cTimer.h"
#include "Utility/Easing/Easing.h"

//==========================================================================================
// 定数
//==========================================================================================
const int	cTouchFont::PRIORITY = 1000;
//==========================================================================================
// コンストラクタ
//==========================================================================================
cTouchFont::cTouchFont(IBaseObject* parent)
	: cSpriteObject(parent, "TouchFont", "data\\graphic\\touch.png"),
	m_fSinAngle ( 0.0f )
{
}

//==========================================================================================
// デストラクタ
//==========================================================================================
cTouchFont::~cTouchFont(void)
{
}

//==========================================================================================
// 初期化
//==========================================================================================
void cTouchFont::Initialize(void)
{
	SetPriority(PRIORITY);

	cGame* game = (cGame*)GetRoot();

	// 中央下あたりに配置
	SetPos(game->GetWindowCenter().x, game->GetWindowHeight() / 4 * 3.0f);

	cSpriteObject::Initialize();
}

//==========================================================================================
// 更新
//==========================================================================================
void cTouchFont::Update(void)
{
	m_fSinAngle++;

	SetAlpha((unsigned int)((sin(DEG_TO_RAD(m_fSinAngle)) + 1.0f) * 127.5f));


	cSpriteObject::Update();
}